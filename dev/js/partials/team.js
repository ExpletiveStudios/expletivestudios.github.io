$("#member-1").click(function(){
	$("#info-1").show();
	$("#info-2").hide();
	$("#info-3").hide();
});

$("#member-2").click(function(){
	$("#info-1").hide();
	$("#info-2").show();
	$("#info-3").hide();
});

